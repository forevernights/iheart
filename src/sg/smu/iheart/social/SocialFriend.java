package sg.smu.iheart.social;

public class SocialFriend {
	public String email;
	public String name;
	public String mobile;
	public String gender;
	public int drawableResource;
	public SocialFriend(String email, String name, String mobile, String gender,int drawableResource) {
		super();
		this.email = email;
		this.name = name;
		this.mobile = mobile;
		this.gender = gender;
		this.drawableResource = drawableResource;
	}
	
}
